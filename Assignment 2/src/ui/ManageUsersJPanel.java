    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        //NUID Validation       
        String NUIDStr = txtEnterNeuID.getText();
        if (NUIDStr.length() != 9 || !NUIDStr.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid 9 Digit NUID.");
            return;
        }
        long NUID = Long.parseLong(txtEnterNeuID.getText());
        
        for(UserInfo ur: userHist.getUserHistory()){
            if(ur.getNUID() == NUID){
                txtUserName.setText(ur.getUserName());
                txtFirstName.setText(ur.getFirstName());
                txtLastName.setText(ur.getLastName()); 
                txtEmail.setText(ur.getEmailID());
            }
            
            else{
                JOptionPane.showMessageDialog(this, "Please Enter Valid NUID");
            }
            
            
        }

    }//GEN-LAST:event_btnSearchActionPerformed

