    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        long NUID = Long.parseLong(txtEnterNeuID.getText()); 
        String Username = String.valueOf(txtUsername.getText());
        String Password = String.valueOf(txtPassword.getText());
        String p = "^(?=.*[A-Za-z])(?=.*\\d)(?=.*[@$!%*?&])[A-Za-z\\d@$!%*?&]{8,}$";
        if(Password == null || Password.equals("") || !(Password.matches(p))){
                JOptionPane.showMessageDialog(this, "Please enter a valid Password");
                return;
            }   
        
        if(checkBoxBlockUser.isSelected()){
            for(UserInfo usr: userHist.getUserHistory()){
                if(usr.getNUID() == NUID){
                    userHist.deleteUser(usr);
                    JOptionPane.showMessageDialog(this, "This User Has Been Blocked.");
                }
            }
        }
         
        for(UserInfo ur: userHist.getUserHistory()){
            
            if(ur.getNUID() == NUID){
                ur.setUserName(Username);
                for(String pw: ur.getPasswordList()){
                    if(ur.pwAuth(pw, Password)){
                        JOptionPane.showMessageDialog(this, "The Password Entered is Already Used.");
                        return;
                    }
                }
                ur.setPasswordList(Password);
                JOptionPane.showMessageDialog(this, "User Credentials are Updated!");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

