    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        
        //NUID
        long NUID = Long.parseLong(txtEnterNeuID.getText());
//        UserInfo userin;
//        String NUIDStr = txtEnterNeuID.getText();
//        if (userin.getNUID) {
//            JOptionPane.showMessageDialog(this, "Please Enter Valid 9 Digit NUID.");
//            return;
//        }
        
        for(UserInfo usr: userHist.getUserHistory()){
            if(usr.getNUID() == NUID){
                txtFirstName.setText(usr.getFirstName());
                txtLastName.setText(usr.getLastName());
                txtEmail.setText(usr.getEmailID());
                txtCountry.setText(usr.getAddress());
                txtContactNumber.setText(String.valueOf(usr.getContactNumber()));
                //txtDOB.setText(prs.getDOB());
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

