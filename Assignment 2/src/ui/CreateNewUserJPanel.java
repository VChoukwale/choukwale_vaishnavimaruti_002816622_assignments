    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        //NUID Validation
        String NUIDStr = txtNUID.getText();
        if (NUIDStr.length() != 9 || !NUIDStr.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid 9 Digit NUID.");
            return;
        }
        long NUID = Long.parseLong(txtNUID.getText());

//        
        //User Name Validation
        String UserName = txtUserName.getText();
        if (!UserName.matches("^[a-zA-Z]+$")) {
            JOptionPane.showMessageDialog(this, "Please Enter Correct User Name.");
            return;
        }
        
        int selectedRowIndex = tblPerson.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select row to display!");
            return;   
        }
        DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
        PersonInfo selectedPerson = (PersonInfo) model.getValueAt(selectedRowIndex, 0);
        String FirstName = selectedPerson.getFirstName();
        String LastName = selectedPerson.getLastName();
        String Address = selectedPerson.getAddress();
        String EmailID = selectedPerson.getEmailID();
        //txtContactNumber.setText(String.valueOf(selectedPerson.getContactNumber()));
        long ContactNumber = Long.parseLong(String.valueOf(selectedPerson.getContactNumber()));
        
        
        //User Password Validation
        String Password = String.valueOf(txtPassword.getPassword());
        String hashedPassword = BCrypt.hashpw(Password, BCrypt.gensalt());
        String pw = "^(?=.*[A-Za-z])(?=.*\\d)(?=.*[@$!%*?&])[A-Za-z\\d@$!%*?&]{8,}$";

        if(Password == null || Password.equals("") || !(Password.matches(pw))){
                JOptionPane.showMessageDialog(this, "Please enter a valid Password");
                return;
            }   
      
        if (checkBoxEnable.isSelected()){
            UserInfo ui = userHist.addNewUser();
            ui.setNUID(NUID);
            ui.setUserName(UserName);
            ui.setPasswordList(Password);
            ui.setFirstName(FirstName);
            ui.setLastName(LastName);
            ui.setAddress(Address);
            ui.setEmailID(EmailID);
            ui.setContactNumber(ContactNumber);
        }
        if (!checkBoxEnable.isSelected()){
            JOptionPane.showMessageDialog(this, "Please Enable User first!");
        }
        
        
        txtNUID.setText("");
        //txtUserID.setText("");
        txtUserName.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_btnAddActionPerformed
    

