    private void populateTable() {
       DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
       model.setRowCount(0);
       
       for(PersonInfo prs: perHist.getPersonHistory()){
           Object[] row = new Object[3];
           row[0] = prs;
           row[1] = prs.getLastName();
           row[2] = prs.getEmailID();
           
           model.addRow(row);
       }
    }
