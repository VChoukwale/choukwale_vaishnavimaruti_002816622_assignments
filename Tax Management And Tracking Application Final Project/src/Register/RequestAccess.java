    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
      
        
        //Username validation
        String username = txtusername.getText();
        if (!username.matches("^[a-zA-Z0-9!@#$%^&*()-_+=<>?/.,]+$") || username.contains(" ") || username.length() < 6 || username.length() > 20) {
           JOptionPane.showMessageDialog(this, "Please Enter a valid username.");
        return;
        }
        
       // Password Validation
        String password = txtpassword.getText(); 
        if (!password.matches("^(?=.*[A-Za-z])(?=.*\\d)(?=.*[@$!%*?&])[A-Za-z\\d@$!%*?&]{8,20}$")) {
            JOptionPane.showMessageDialog(this, "Please Enter a Strong Password with a length between 8 and 20 characters.");
        return; // Stop execution if validation fails
        }
        
        // Full Name Validation
        String fullName = txtfullname.getText();
        if (!fullName.matches("^[a-zA-Z ]+$") || fullName.length() < 3 || fullName.length() > 30) {
            JOptionPane.showMessageDialog(this, "Please Enter a valid full name.\nFull name should only contain letters and spaces, and be between 3 and 30 characters.");
        return;
        }
                
        //Contact Number Validation
        String ContactNumberStr = txtphonenumber.getText();
        if (ContactNumberStr.length() != 10 || !ContactNumberStr.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid 10 Digit Contact Number.");
            return;
        }
        long ContactNumber = Long.parseLong(txtphonenumber.getText());
         
        
        //Address Validation
        String Address = txtaddress.getText(); 
        if (!Address.matches("^[a-zA-Z0-9 ]+$")) {
            JOptionPane.showMessageDialog(this, "Please enter valid address.");
        return; // Stop execution if validation fails
        }
        
        // Fingerprint Scans Validation
         String fingerprintScan = (String) Combofingerprint.getSelectedItem();
        if (!fingerprintScan.equals("Completed")) {
            JOptionPane.showMessageDialog(this, "Please complete Fingerprint Scans before submitting the request.");
        return;
        }

        // Eye Scan Validation
         String eyeScan = (String) Comboeyescan.getSelectedItem();
        if (!eyeScan.equals("Completed")) {
            JOptionPane.showMessageDialog(this, "Please complete Eye Scan before submitting the request.");
        return;
        }
         
        saveUserDetails();
        // Role Validation
//        String role = (String) Comborole.getSelectedItem();
//        if (!role.equals("Admin") && !role.equals("User")) {
//            JOptionPane.showMessageDialog(this, "Please select a valid role (Admin or User).");
//        return;
//        }
        
          

    }//GEN-LAST:event_btnSubmitActionPerformed

