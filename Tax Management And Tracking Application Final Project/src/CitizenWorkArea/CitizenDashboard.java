    private void updateTextFieldsFromDivisionTable() {
    try {
         Connection connection = (Connection)DatabaseConnection.getConnection();
        String sql = "SELECT * FROM division WHERE budgetid = ?";
        PreparedStatement preparedStatement = (PreparedStatement) connection.prepareStatement(sql);
        preparedStatement.setInt(1, Integer.parseInt(txtbudgetid.getText()));

        ResultSet resultSet = preparedStatement.executeQuery();

        while (resultSet.next()) {
            String itemName = resultSet.getString("division");
            String budgetAmount = resultSet.getString("budgetamountallocated");

            switch (itemName) {
                case "Cancer_Buidling":
                    txtcancerbuilding.setText(budgetAmount);
                    break;
                case "Covid":
                    txtcovidvaccine.setText(budgetAmount);
                    break;
                case "Defibrillators":
                    txtdefibrilllators.setText(budgetAmount);
                    break;
                case "Doctor_Salaries":
                    txtdoctorsalary.setText(budgetAmount);
                    break;
                case "ECG_Machines":
                    txtecgmachines.setText(budgetAmount);
                    break;
                case "Tanks":
                    txtTanks.setText(budgetAmount);
                    break;
                case "Heart_Building":
                    txtheartbuilding.setText(budgetAmount);
                    break;
                case "Military_Fuel":
                    txtmilitary.setText(budgetAmount);
                    break;
                case "Military_Gas":
                    txtschoolbuilding.setText(budgetAmount);
                    break;
                // Add more cases for each item as needed
            }
        }

        resultSet.close();
        preparedStatement.close();
      
    } catch (SQLException ex) {
        ex.printStackTrace();
        // Handle SQLException as needed
    }
}



