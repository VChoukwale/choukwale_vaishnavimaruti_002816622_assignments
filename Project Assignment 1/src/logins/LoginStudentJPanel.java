    private void stubtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stubtnLoginActionPerformed
        // TODO add your handling code here:
        try{
            
            Connection connection = (Connection)DatabaseConnection.getConnection();
            String username = stutxtUsername.getText();
            String password = stuPassword.getText();
            
            
           String sql = "SELECT NUID, hashedpassword FROM users WHERE username = ? AND profile_type = 'Student'";
            String insertStudent = "INSERT INTO student (nuid) VALUES (?)";
            PreparedStatement preparedStatement = connection.prepareStatement(sql);
            preparedStatement.setString(1, username);

            
             
            
            PreparedStatement professorPreparedStatement = connection.prepareStatement(insertStudent);
            
            
            
            ResultSet rs = preparedStatement.executeQuery();
            if (rs.next()) {
                String selectedNUID = rs.getString("NUID");
                String hashedPasswordFromDB = rs.getString("hashedpassword");

                createuserprofile userProfile = new createuserprofile();

                if (userProfile.checkPassword(password, hashedPasswordFromDB)) {
                    System.out.println("selectedNUID: " + selectedNUID);
                    StudentLandingJPanel stulanding = new StudentLandingJPanel(mainframe, selectedNUID);
                    mainframe.setRightComponent(stulanding);
                } else {
                    System.out.println("Login failed");
                    JOptionPane.showMessageDialog(this, "Username or password is incorrect");
                    stutxtUsername.setText("");
                    stuPassword.setText("");
                }
            } else {
                System.out.println("User not found");
                JOptionPane.showMessageDialog(this, "Username or password is incorrect");
                stutxtUsername.setText("");
                stuPassword.setText("");
            }

            
            professorPreparedStatement.setInt(1,Integer.parseInt(selectedNUID));

               // Execute the professor INSERT query
             professorPreparedStatement.executeUpdate();

           
            
       
             
        }catch(Exception e){
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_stubtnLoginActionPerformed

