    private void btnDonateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDonateActionPerformed
        // TODO add your handling code here:
          String donationAmount = txtDonate.getText();

    if (donationAmount.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a donation amount.");
        return;
    }

    try {
        Connection connection = (Connection) DatabaseConnection.getConnection();

        String sql = "INSERT INTO donations (amount) VALUES (?)";
        PreparedStatement preparedStatement = connection.prepareStatement(sql);
        preparedStatement.setString(1, donationAmount);

        int rowsInserted = preparedStatement.executeUpdate();

        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Donation added successfully.");
            txtDonate.setText(""); // Clear the text field
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add donation.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        preparedStatement.close();
        // Close the connection if needed

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error adding donation.", "Error", JOptionPane.ERROR_MESSAGE);
    }
        
    }//GEN-LAST:event_btnDonateActionPerformed

