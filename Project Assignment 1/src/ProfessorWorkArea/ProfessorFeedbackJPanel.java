private void populateFeedbackTable() {
    DefaultTableModel model = (DefaultTableModel) tblFeedback.getModel();
    model.setRowCount(0); // Clear existing data in the table

    try {
        Connection connection = (Connection) DatabaseConnection.getConnection();
        String sql = "SELECT courseid, coursename, feedback FROM feedback";
        PreparedStatement preparedStatement = connection.prepareStatement(sql);

        ResultSet resultSet = preparedStatement.executeQuery();

        while (resultSet.next()) {
            Object[] row = {
                resultSet.getInt("courseid"),
                resultSet.getString("coursename"),
                resultSet.getString("feedback")
            };
            model.addRow(row);
        }

        resultSet.close();
        preparedStatement.close();
        // Close the connection if needed

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error retrieving feedback data.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}

