    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String feedbackText = txtFeedback.getText();

    if (feedbackText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter feedback before submitting.");
        return;
    }

    try {
        Connection connection = (Connection) DatabaseConnection.getConnection();

       
        String sql = "INSERT INTO feedback (courseid, feedback,nuid,coursename) VALUES (?, ?,?,?)";
        PreparedStatement preparedStatement = connection.prepareStatement(sql);
        System.out.println("Selected COURSE Feedabck: " + selectedCourse);
        preparedStatement.setInt(1, Integer.parseInt(selectedCourse)); // Set the course ID
        preparedStatement.setString(2, feedbackText); // Set the feedback text
        preparedStatement.setInt(3, Integer.parseInt(selectedNUID)); 
        preparedStatement.setString(4,CourseName); 

        int rowsInserted = preparedStatement.executeUpdate();

        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Feedback added successfully.");
            txtFeedback.setText(""); // Clear the feedback text field
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add feedback.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        preparedStatement.close();
        // Close the connection if needed

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error adding feedback.", "Error", JOptionPane.ERROR_MESSAGE);
    }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

